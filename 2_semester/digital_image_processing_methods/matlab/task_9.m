%% Задание 9. Логические операторы для v1 и v2
N = 6;                         % на случай запуска секции отдельно
v1 = [N 2*N 3*N];              % [6 12 18]
v2 = [4*N 5*N 6*N];            % [24 30 36]
and(v1, v2)    % поэлементное И  -> [1 1 1]
v1 & v2        % то же через знак &
or(v1, v2)     % поэлементное ИЛИ -> [1 1 1]
v1 | v2        % то же через знак |
not(v1)        % НЕ -> [0 0 0]
~v2            % то же через знак ~