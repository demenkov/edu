%% Задание 2. Комплексное число Z = N + i*(N+10)
N = 6;
Z = N + 1i*(N + 10)        % Z = 6 + 16i
ReZ = real(Z)              % действительная часть = 6
ImZ = imag(Z)              % мнимая часть = 16
modZ = abs(Z)              % модуль = sqrt(6^2+16^2) = 17.0880
argZ = angle(Z)            % фаза в радианах = 1.2120
argZ_deg = angle(Z)*180/pi % фаза в градусах = 69.444